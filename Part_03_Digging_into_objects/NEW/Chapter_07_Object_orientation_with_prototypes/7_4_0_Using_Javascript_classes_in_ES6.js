/** 7.4 - Using Javascript "classes" in ES6 */

/**
 * It's great that Javascript lets us a form of inheritance via prototypes. But many
 * developers, especially those come from a classical object-oriented background, would prefer
 * a simplification or abstraction of Javascript's inheritance system into one that they're
 * more familiar with.
 */

/**
 * This inevitably leads toward the realm of classes, even though Javascript doesn't
 * support classical inheritance natively. As a response to this need, several Javascript
 * libraries that simulate classical inheritance popped up. Because each library implements
 * classes in its own way, the ECMAScript committee has standardized the syntax for
 * simulating class-based inheritance. Notice how we said simulating. Even though now we
 * can use the class keyword in Javascript, the underlying implementation is still
 * based on PROTOTYPE inheritance!
 */

